public class looping {
    public static void main(String[] args) {
        
        //for loop
        for(int b=0;b<=10;b++){
            System.out.println(b);
        }
        //while loop 
        int c=0;
        while(c<10)
        {
            System.out.println(c);
            c++;
        }
        //do while loop 
        int d=0;
        do{
            System.out.println(d);
            d++;
        }while(d<=10);

    }
}
